% ejemplo4
n=-21:15
subplot(3,1,1)
x1=Esc(-20,n)-Esc(10,n)
stem(n,x1,'filled')
ylim([-1 2])
title('$x_1(n)=u(n+20)-u(n-10)$',Interpreter='latex')
ylabel('x1'),xlabel('n')
subplot(3,1,2)
x2=exp(0.1*n)
stem(n,x2)
ylabel('x2'),xlabel('n')
title('$x_2(n)=e^{0.1n}$',Interpreter='latex')
subplot(3,1,3)
[x3,n]=sigmul(x1,n,x2,n)
stem(n,x3,"filled",'g')
ylim([0 5])
ylabel('xn'),xlabel('n')
title('$x_3(n)=x_1(n)x_2(n)$',Interpreter='latex')